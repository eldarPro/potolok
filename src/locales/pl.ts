export const pl: Locale = {
  strings: s,
  roomsPlural: (n: number): string => {
    if (n === 1) return s['rooms.one'];
    if ([2, 3, 4].includes(n % 10) && ![12, 13, 14].includes(n % 100)) return s['rooms.few'];
    return s['rooms.many'];
  },
};
